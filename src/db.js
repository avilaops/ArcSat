import mongoose from "mongoose";
import dotenv from "dotenv";
dotenv.config();

// Configura√ß√µes otimizadas para Azure Cosmos DB (MongoDB API)
const mongooseOptions = {
  maxPoolSize: 10,
  minPoolSize: 2,
  serverSelectionTimeoutMS: 5000,
  socketTimeoutMS: 45000,
  family: 4, // Use IPv4
  retryWrites: true,
  retryReads: true,
  w: 'majority'
};

export const connectDB = async () => {
  try {
    const mongoUri = process.env.MONGODB_URI || process.env.MONGO_URI;
    
    if (!mongoUri) {
      console.error("‚ùå MONGODB_URI n√£o configurado no ambiente");
      process.exit(1);
    }

    // Conectar ao MongoDB/Cosmos DB
    await mongoose.connect(mongoUri, mongooseOptions);
    
    console.log("‚úÖ MongoDB conectado com sucesso");
    console.log(`üìç Database: ${mongoose.connection.db.databaseName}`);
    
    // Event listeners para monitoramento
    mongoose.connection.on('error', (err) => {
      console.error('‚ùå Erro no MongoDB:', err);
    });
    
    mongoose.connection.on('disconnected', () => {
      console.warn('‚ö†Ô∏è  MongoDB desconectado');
    });
    
    mongoose.connection.on('reconnected', () => {
      console.log('üîÑ MongoDB reconectado');
    });
    
  } catch (err) {
    console.error("‚ùå Erro ao conectar MongoDB:", err.message);
    console.error("üí° Verifique se MONGODB_URI est√° configurado corretamente");
    process.exit(1);
  }
};

// Fun√ß√£o para verificar status da conex√£o
export const isConnected = () => {
  return mongoose.connection.readyState === 1;
};

// Fun√ß√£o para fechar conex√£o graciosamente
export const closeDB = async () => {
  try {
    await mongoose.connection.close();
    console.log('MongoDB conex√£o fechada');
  } catch (err) {
    console.error('Erro ao fechar MongoDB:', err);
  }
};