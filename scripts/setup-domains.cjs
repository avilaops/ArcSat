// Fun√ß√£o principal
async function setupDomains() {
  try {
    console.log('\nüéØ Obtendo Zone ID...');
    const zoneId = await getZoneId();
    
    console.log(`\nüîß Configurando ${subdomains.length} registros DNS...\n`);
    
    // Configurar cada subdom√≠nio
    const results = [];
    for (let i = 0; i < subdomains.length; i++) {
      const subdomain = subdomains[i];
      console.log(`[${i + 1}/${subdomains.length}] Processando ${subdomain.name}...`);
      
      const result = await createDNSRecord(zoneId, subdomain);
      results.push({ subdomain: subdomain.name, success: !!result });
      
      // Pequena pausa entre requisi√ß√µes
      await new Promise(resolve => setTimeout(resolve, 1000));
    }
    
    // Relat√≥rio final
    console.log('\n' + '='.repeat(50));
    console.log('üìä RELAT√ìRIO FINAL');
    console.log('='.repeat(50));
    
    const successful = results.filter(r => r.success).length;
    const failed = results.filter(r => !r.success).length;
    
    console.log(`‚úÖ Configurados com sucesso: ${successful}`);
    console.log(`‚ùå Falhas: ${failed}`);
    
    console.log('\nüåê Status dos subdom√≠nios:');
    results.forEach(result => {
      const status = result.success ? '‚úÖ' : '‚ùå';
      console.log(`  ${status} ${result.subdomain}`);
    });
    
    if (successful === subdomains.length) {
      console.log('\nüéâ Todos os dom√≠nios foram configurados com sucesso!');
      console.log('‚è≥ A propaga√ß√£o DNS pode levar at√© 24 horas para ser conclu√≠da.');
      console.log('üîó Voc√™ pode verificar a propaga√ß√£o em: https://dnschecker.org/');
    } else {
      console.log('\n‚ö†Ô∏è Alguns dom√≠nios falharam na configura√ß√£o.');
      console.log('üìù Verifique os logs acima para mais detalhes.');
    }
    
    // Salvar relat√≥rio
    const reportPath = path.join(__dirname, '..', 'domain-setup-report.json');
    const report = {
      timestamp: new Date().toISOString(),
      domain: BASE_DOMAIN,
      zoneId,
      results,
      summary: {
        total: subdomains.length,
        successful,
        failed
      }
    };
    
    fs.writeFileSync(reportPath, JSON.stringify(report, null, 2));
    console.log(`\nüìÑ Relat√≥rio salvo em: ${reportPath}`);
    
  } catch (error) {
    console.error(`\nüí• Erro cr√≠tico: ${error.message}`);
    console.error(error);
    process.exit(1);
  }
}

