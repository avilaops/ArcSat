// Fun√ß√£o para fazer requisi√ß√µes HTTP
function makeRequest(options, data = null) {
  return new Promise((resolve, reject) => {
    const req = https.request(options, (res) => {
      let responseData = '';
      
      res.on('data', (chunk) => {
        responseData += chunk;
      });
      
      res.on('end', () => {
        try {
          const parsedData = JSON.parse(responseData);
          resolve({
            statusCode: res.statusCode,
            data: parsedData
          });
        } catch (error) {
          resolve({
            statusCode: res.statusCode,
            data: responseData
          });
        }
      });
    });
    
    req.on('error', (error) => {
      reject(error);
    });
    
    if (data) {
      req.write(JSON.stringify(data));
    }
    
    req.end();
  });
}

// Fun√ß√£o para obter o Zone ID se n√£o estiver configurado
async function getZoneId() {
  if (ZONE_ID) {
    console.log(`‚úÖ Zone ID encontrado: ${ZONE_ID}`);
    return ZONE_ID;
  }
  
  console.log('üîç Buscando Zone ID para o dom√≠nio...');
  
  const options = {
    hostname: 'api.cloudflare.com',
    path: '/client/v4/zones',
    method: 'GET',
    headers: {
      'Authorization': `Bearer ${CLOUDFLARE_API_TOKEN}`,
      'Content-Type': 'application/json'
    }
  };
  
  try {
    const response = await makeRequest(options);
    
    if (response.statusCode === 200 && response.data.success) {
      const zones = response.data.result;
      const zone = zones.find(z => z.name === BASE_DOMAIN);
      
      if (zone) {
        console.log(`‚úÖ Zone ID encontrado: ${zone.id}`);
        return zone.id;
      } else {
        throw new Error(`Dom√≠nio ${BASE_DOMAIN} n√£o encontrado na Cloudflare`);
      }
    } else {
      throw new Error(`Erro na API: ${JSON.stringify(response.data)}`);
    }
  } catch (error) {
    console.error(`‚ùå Erro ao buscar Zone ID: ${error.message}`);
    throw error;
  }
}

// Fun√ß√£o para criar/atualizar DNS record
async function createDNSRecord(zoneId, record) {
  console.log(`üîß Configurando ${record.name}...`);
  
  // Primeiro, verificar se o record j√° existe
  const listOptions = {
    hostname: 'api.cloudflare.com',
    path: `/client/v4/zones/${zoneId}/dns_records?name=${record.name}&type=${record.type}`,
    method: 'GET',
    headers: {
      'Authorization': `Bearer ${CLOUDFLARE_API_TOKEN}`,
      'Content-Type': 'application/json'
    }
  };
  
  try {
    const listResponse = await makeRequest(listOptions);
    
    if (listResponse.statusCode === 200 && listResponse.data.success) {
      const existingRecords = listResponse.data.result;
      
      if (existingRecords.length > 0) {
        // Record existe, atualizar
        const existingRecord = existingRecords[0];
        const updateOptions = {
          hostname: 'api.cloudflare.com',
          path: `/client/v4/zones/${zoneId}/dns_records/${existingRecord.id}`,
          method: 'PUT',
          headers: {
            'Authorization': `Bearer ${CLOUDFLARE_API_TOKEN}`,
            'Content-Type': 'application/json'
          }
        };
        
        const updateResponse = await makeRequest(updateOptions, record);
        
        if (updateResponse.statusCode === 200 && updateResponse.data.success) {
          console.log(`‚úÖ ${record.name} atualizado com sucesso`);
          return updateResponse.data.result;
        } else {
          console.error(`‚ùå Erro ao atualizar ${record.name}:`, updateResponse.data);
          return null;
        }
      } else {
        // Record n√£o existe, criar
        const createOptions = {
          hostname: 'api.cloudflare.com',
          path: `/client/v4/zones/${zoneId}/dns_records`,
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${CLOUDFLARE_API_TOKEN}`,
            'Content-Type': 'application/json'
          }
        };
        
        const createResponse = await makeRequest(createOptions, record);
        
        if (createResponse.statusCode === 200 && createResponse.data.success) {
          console.log(`‚úÖ ${record.name} criado com sucesso`);
          return createResponse.data.result;
        } else {
          console.error(`‚ùå Erro ao criar ${record.name}:`, createResponse.data);
          return null;
        }
      }
    } else {
      console.error(`‚ùå Erro ao listar records:`, listResponse.data);
      return null;
    }
  } catch (error) {
    console.error(`‚ùå Erro ao configurar ${record.name}: ${error.message}`);
    return null;
  }
}

